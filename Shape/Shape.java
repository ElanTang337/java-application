/**
 * This this a class representing different shapes.
 * @author siyut
 */
public abstract class Shape {
    /**
     * a constructor.
     */
    public Shape() {

    }
    /**
     * an abstract method of getarea.
     * @return
     * return the value of area.
     */
    public abstract double getArea();
    /**
     * an abstract method of getperimeter.
     * @return
     * return the value of perimeter
     */
    public abstract double getPerimeter();
}
